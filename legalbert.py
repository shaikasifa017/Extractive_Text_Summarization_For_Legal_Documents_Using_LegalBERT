def summarize_document(original_doc, ratio=0.40, lambda_param=0.7):

    # Sentence Splitting
    sentences_original = sent_tokenize(original_doc)
    sentences_original = [s.strip() for s in sentences_original if len(s.strip()) > 20]

    if len(sentences_original) < 2:
        return original_doc

    # Clean for embeddings
    def clean_sentence(text):
        text = text.lower()
        text = re.sub(r'[^a-z0-9 ]', '', text)
        text = re.sub(r'\s+', ' ', text)
        return text.strip()

    sentences_clean = [clean_sentence(s) for s in sentences_original]

    # Generate embeddings
    sentence_embeddings = np.array(
        [get_sentence_embedding(s) for s in sentences_clean]
    )

    # ---------------------------
    # TextRank Importance (FIXED)
    # ---------------------------
    similarity_matrix = cosine_similarity(sentence_embeddings)

    # Remove self-similarity dominance
    np.fill_diagonal(similarity_matrix, 0)

    nx_graph = nx.from_numpy_array(similarity_matrix)

    try:
        scores = nx.pagerank(nx_graph, max_iter=500, tol=1e-06)
    except:
        print("PageRank failed, using fallback uniform scores.")
        scores = {i: 1.0 for i in range(len(sentences_original))}

    # ---------------------------
    # Clustering (Structure)
    # ---------------------------
    num_sentences = len(sentences_original)
    k = min(4, num_sentences)

    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10)
    cluster_labels = kmeans.fit_predict(sentence_embeddings)

    selected = []

    # Select one representative per cluster
    for cluster_id in range(k):
        cluster_indices = [i for i in range(num_sentences)
                           if cluster_labels[i] == cluster_id]
        if cluster_indices:
            best_idx = max(cluster_indices, key=lambda i: scores[i])
            selected.append(best_idx)

    # ---------------------------
    # Word limit (40%)
    # ---------------------------
    original_word_count = len(original_doc.split())
    word_limit = int(original_word_count * ratio)

    current_word_count = sum(len(sentences_original[i].split()) for i in selected)

    # ---------------------------
    # MMR Refinement
    # ---------------------------
    remaining = [i for i in range(num_sentences) if i not in selected]

    while remaining and current_word_count < word_limit:

        mmr_scores = []

        for i in remaining:
            importance = scores[i]

            similarity_to_selected = max(
                cosine_similarity(
                    [sentence_embeddings[i]],
                    [sentence_embeddings[j]]
                )[0][0]
                for j in selected
            )

            mmr_score = lambda_param * importance - (1 - lambda_param) * similarity_to_selected

            mmr_scores.append((mmr_score, i))

        mmr_scores.sort(reverse=True)
        best_score, best_idx = mmr_scores[0]

        sentence_length = len(sentences_original[best_idx].split())

        if current_word_count + sentence_length <= word_limit:
            selected.append(best_idx)
            current_word_count += sentence_length

        remaining.remove(best_idx)

    selected = sorted(selected)

    summary = " ".join([sentences_original[i] for i in selected])

    return summary

