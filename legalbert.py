def get_sentence_embeddings_batch(sentences, batch_size=16):
    all_embeddings = []

    for i in range(0, len(sentences), batch_size):
        batch = sentences[i:i+batch_size]

        inputs = tokenizer(
            batch,
            padding=True,
            truncation=True,
            max_length=128,
            return_tensors="pt"
        ).to(device)

        with torch.no_grad():
            outputs = model(**inputs)

        emb = outputs.last_hidden_state[:, 0, :]
        all_embeddings.append(emb.cpu())

    return torch.cat(all_embeddings).numpy()

def textrank_scores(sim, iters=20, d=0.85):
    n = sim.shape[0]
    scores = np.ones(n) / n
    sim = sim / (sim.sum(axis=1, keepdims=True) + 1e-9)

    for _ in range(iters):
        scores = (1 - d) + d * sim.T @ scores

    return scores

def is_boilerplate(sentence):
    patterns = [
        r'case\s+\d',
        r'document\s+\d',
        r'filed\s+\d',
        r'entered:',
        r'pacer',
        r'docket',
        r'civil cover sheet',
        r'summons',
        r'attachment',
        r'u\.s\. district court',
        r'clerk of court',
        r'notice of electronic filing'
    ]
    sentence = sentence.lower()
    return any(re.search(p, sentence) for p in patterns)

def is_legal_noise(sentence):
    s = sentence.lower()

    # address blocks
    if re.search(r'\b(po box|box \d+|street|avenue|road|suite|ca \d{5}|dc \d{5})\b', s):
        return True

    # phone numbers
    if re.search(r'\(\d{3}\)\s*\d{3}[-–]\d{4}', s):
        return True

    # party captions (short lines with roles)
    if re.search(r'plaintiff|defendant|petitioner|respondent', s) and len(s.split()) < 15:
        return True

    # ALL CAPS headings
    if sentence.isupper() and len(sentence.split()) < 20:
        return True

    return False

def summarize_document_fast(doc, ratio=0.4, lambda_param=0.7):

    sentences = [
    s.strip()
    for s in sent_tokenize(doc)
    if 10 <= len(s.split()) <= 40
    and not is_boilerplate(s)
    and not is_legal_noise(s)
]
    if len(sentences) < 2:
        return doc

    def clean(s):
        s = s.lower()
        s = re.sub(r'[^a-z0-9 ]', '', s)
        return s

    clean_sentences = [clean(s) for s in sentences]

    # ---- Batch embeddings ----
    embeddings = get_sentence_embeddings_batch(clean_sentences)

    # ---- Similarity ----
    sim_matrix = cosine_similarity(embeddings)
    np.fill_diagonal(sim_matrix, 0)

    # ---- Importance ----
    scores = textrank_scores(sim_matrix)

    # ---- Clustering ----
    k = min(4, len(sentences))
    labels = KMeans(n_clusters=k, random_state=42, n_init=5)\
                .fit_predict(embeddings)

    selected = []
    for c in range(k):
        idx = np.where(labels == c)[0]
        best = idx[np.argmax(scores[idx])]
        selected.append(best)

    # ---- Word limit ----
    word_limit = int(len(doc.split()) * ratio)
    current_words = sum(len(sentences[i].split()) for i in selected)

    remaining = list(set(range(len(sentences))) - set(selected))

    # ---- FAST MMR (precomputed similarity) ----
    while remaining and current_words < word_limit:
        mmr_vals = []

        for i in remaining:
            sim_to_sel = max(sim_matrix[i][j] for j in selected)
            mmr = lambda_param * scores[i] - (1 - lambda_param) * sim_to_sel
            mmr_vals.append((mmr, i))

        _, best = max(mmr_vals)

        sent_len = len(sentences[best].split())
        if current_words + sent_len <= word_limit:
            selected.append(best)
            current_words += sent_len

        remaining.remove(best)

    selected.sort()
    return " ".join(sentences[i] for i in selected)

